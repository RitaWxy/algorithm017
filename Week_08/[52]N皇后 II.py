class Solution:
    def totalNQueens(self, n: int) -> int:
        if n < 1: return []
        self.count = 0
        self.bit_DFS(n,0,0,0,0)
        return self.count

    def bit_DFS(self,n,level,col,pie,na):
        if level >= n:
            self.count += 1
